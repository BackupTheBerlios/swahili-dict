<h1>Hilfeseite</h1>

<h2>So wird gesucht</h2>
<p>Gesucht wird nach allen Wörterbucheinträgen, in denen der Suchbegriff enthalten ist. 
    Auch wenn es sich beim Suchbegriff nur um einen Wortteil handelt. </p>
<p><b>Beispiele:</b> <br />
    "fuata" findet sowohl "-fuata" als auch "-fuatana" etc.<br />
    "a" findet alle Wörter/Begriffe in denen der Buchstabe a oder A enthalten ist
</p>


<h2>Hinweise zur Benutzung</h2>
<ul>
    <li>Fakultative Bestandteile werden in eckigen Klammern aufgeführt.<br />
    Beispiel: "fundi [wa] kompyuta" heisst: "fundi wa kompyuta" oder "fundi kompyuta"</li>
    <li>Bei Substantiven wird der Plural in runden Klammern aufgeführt. (Ausnahme: Substantive, die nur im Singular existieren.)<br />
    Beispiel: chakula (vyakula)</li>
    <li>Substantive, die mehrere Pluralformen aufweisen, werden getrennt aufgeführt.</li>
    <li>Bei Substantiven, die nur im Singular oder nur im Plural vorliegen, wird <span style="font-style:italic">(Sg) bzw. (Pl)</span> angehängt.</li>
    <li>Bei Verben wird der Verbstamm mit einem Bindestrich angegeben<br />
    Beispiel: -fanya</li>

</ul>

